package JavaSampleCodes;

public class whileEvenOddDesc {

	public static void main(String[] args) {
		// even and odd in desc order
		
		int i=10;
		
		while(i>=1)
		{
			if(i%2==0)
			{
			System.out.println(i   + "Even");	
			}else
			{
			System.out.println(i  + "Odd");	
			
			}
			
			i--;
					
			
			}

	}

}
